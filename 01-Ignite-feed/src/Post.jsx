// Define o componente Post, que representa um post simples na aplicação.
function Post() {
    // Retorna um elemento de parágrafo <p> com o texto "Post".
    // Esse é o conteúdo que será exibido na tela quando o componente for renderizado.
    return <p>Post</p>;
}

// Exporta o componente Post como a exportação padrão deste módulo.
// Isso permite que outros arquivos importem o componente sem usar chaves, por exemplo:
// import Post from './Post';
export default Post; 
